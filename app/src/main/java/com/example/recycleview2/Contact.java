package com.example.recycleview2;

public class Contact {
    private String nama, status, tipePhone;
    private Integer image_view;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTipePhone() {
        return tipePhone;
    }

    public void setTipePhone(String tipePhone) {
        this.tipePhone = tipePhone;
    }

    public Integer getImage_view() {
        return image_view;
    }

    public void setImage_view(Integer image_view) {
        this.image_view = image_view;
    }
}
